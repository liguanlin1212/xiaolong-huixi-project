class ExplorePromptBuilder:
    def __init__(self):
        self.base_prompt = """
你是一个专业的信息检索助手，负责为用户提供特定领域的已结案事件列表。

请给我十个{domain}领域已结案的事件，要求：
1. 事件必须是已结案的，有明确的最终结论
2. 每个事件需要包含：
   - 事件名称
   - 时间范围
   - 简要描述
   - 结案状态
   - 判断演变过程（至少3个时间点）
   - 关键证据（至少3条）
   - 最终结论
   - 信息来源（至少3个）
3. 事件应该具有一定的影响力和代表性
4. 时间范围尽量覆盖近3年内的事件
5. 请按照以下格式返回：

## 事件1：[事件名称]
- 时间范围：[开始时间] 至 [结束时间]
- 简要描述：[简要描述]
- 结案状态：已结案
- 判断演变过程：
  1. [时间点1]：[判断内容] - [依据]
  2. [时间点2]：[判断内容] - [依据]
  3. [时间点3]：[判断内容] - [依据]
- 关键证据：
  1. [证据1]
  2. [证据2]
  3. [证据3]
- 最终结论：[最终结论]
- 信息来源：
  1. [来源1]
  2. [来源2]
  3. [来源3]

## 事件2：[事件名称]
...

以此类推，共10个事件。
"""
    
    def build_prompt(self, domain):
        """构建探索指令"""
        return self.base_prompt.format(domain=domain)
    
    def build_refine_prompt(self, domain, previous_results):
        """构建精炼探索指令"""
        refine_prompt = """
你是一个专业的信息检索助手，负责为用户提供特定领域的已结案事件列表。

我之前请求了{domain}领域的已结案事件，得到了以下结果：
{previous_results}

请提供更多{domain}领域的已结案事件，要求：
1. 事件必须是已结案的，有明确的最终结论
2. 每个事件需要包含：
   - 事件名称
   - 时间范围
   - 简要描述
   - 结案状态
   - 判断演变过程（至少3个时间点）
   - 关键证据（至少3条）
   - 最终结论
   - 信息来源（至少3个）
3. 事件应该具有一定的影响力和代表性
4. 时间范围尽量覆盖近3年内的事件
5. 请提供与之前不同的事件
6. 请按照以下格式返回：

## 事件1：[事件名称]
- 时间范围：[开始时间] 至 [结束时间]
- 简要描述：[简要描述]
- 结案状态：已结案
- 判断演变过程：
  1. [时间点1]：[判断内容] - [依据]
  2. [时间点2]：[判断内容] - [依据]
  3. [时间点3]：[判断内容] - [依据]
- 关键证据：
  1. [证据1]
  2. [证据2]
  3. [证据3]
- 最终结论：[最终结论]
- 信息来源：
  1. [来源1]
  2. [来源2]
  3. [来源3]

## 事件2：[事件名称]
...

以此类推，共10个事件。
"""
        return refine_prompt.format(domain=domain, previous_results=previous_results)