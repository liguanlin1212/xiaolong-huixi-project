class SearchPromptBuilder:
    def __init__(self):
        self.base_prompt = """
你是一个专业的信息检索助手，负责全网搜索事件的相关信息。

请针对以下事件进行全面搜索，重点关注：
1. 事件的基本信息（时间、地点、涉及人物/组织）
2. 事件的发展过程和时间线
3. 公众和专家对事件的判断演变
4. 事件的最终结论或当前状态
5. 支持各阶段判断的关键证据

事件关键词：{query}

请按照以下格式返回搜索结果：

## 事件概述
- 事件名称：[事件名称]
- 时间范围：[开始时间] 至 [结束时间]
- 事件描述：[简要描述]
- 结案状态：[已结案/未结案]

## 判断演变过程
1. [时间点1]：[判断内容] - [依据]
2. [时间点2]：[判断内容] - [依据]
3. [时间点3]：[判断内容] - [依据]
...

## 关键证据
1. [证据1]
2. [证据2]
3. [证据3]
...

## 最终结论
[最终结论内容]

## 信息来源
[来源1]
[来源2]
[来源3]
...
"""
    
    def build_prompt(self, query):
        """构建搜索指令"""
        return self.base_prompt.format(query=query)
    
    def build_refine_prompt(self, initial_query, previous_results):
        """构建精炼搜索指令"""
        refine_prompt = """
你是一个专业的信息检索助手，负责全网搜索事件的相关信息。

我之前搜索了以下事件：{query}

之前的搜索结果如下：
{previous_results}

请对这个事件进行更深入的搜索，补充以下信息：
1. 更详细的时间线
2. 更多的专家观点
3. 更全面的证据
4. 事件的最新进展
5. 不同观点的对比

请按照以下格式返回精炼后的搜索结果：

## 事件概述
- 事件名称：[事件名称]
- 时间范围：[开始时间] 至 [结束时间]
- 事件描述：[简要描述]
- 结案状态：[已结案/未结案]

## 判断演变过程
1. [时间点1]：[判断内容] - [依据]
2. [时间点2]：[判断内容] - [依据]
3. [时间点3]：[判断内容] - [依据]
...

## 关键证据
1. [证据1]
2. [证据2]
3. [证据3]
...

## 最终结论
[最终结论内容]

## 信息来源
[来源1]
[来源2]
[来源3]
...
"""
        return refine_prompt.format(query=initial_query, previous_results=previous_results)
    
    def build_case_status_prompt(self, event_info):
        """构建结案状态判断指令"""
        status_prompt = """
你是一个专业的事件分析专家，负责判断事件的结案状态。

请根据以下事件信息，判断该事件是否已结案：

事件信息：
{event_info}

判断标准：
1. 已结案：事件有明确的最终结论，相关方已发布官方声明，且在过去3个月内没有重要的新进展
2. 未结案：事件仍在发展中，没有明确的最终结论，或在过去3个月内有重要的新进展

请返回：
- 结案状态：[已结案/未结案]
- 判断依据：[详细说明判断理由]
"""
        return status_prompt.format(event_info=event_info)